import java.util.*;

public class X_less_than_Y {
  public static void main(String[] args) {
    Scanner sc=new Scanner(System.in);
    System.out.print("Enter the value of x: ");
    int x=sc.nextInt();
    System.out.print("Enter the value of y: ");
    int y=sc.nextInt();
    System.out.println(x<y);
  }
}
